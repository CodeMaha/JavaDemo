package Introduction;

public class ImplicitCastingExample {
    public static void main(String[] args) {
        // Step 1: Declare an integer variable
        int a = 10;
        // Step 2: Assign int to double (implicit casting happens automatically)
        double b = a;
        // Step 3: Print both values
        System.out.println("Value of a (int): " + a);
        System.out.println("Value of b (double after casting): " + b);
    }
}
