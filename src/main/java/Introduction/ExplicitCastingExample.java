package Introduction;

public class ExplicitCastingExample {

    public static void main(String[] args) {

        // Step 1: Declare a double variable
        double x = 9.8;

        // Step 2: Explicitly cast double to int
        int y = (int) x;

        // Step 3: Print both values
        System.out.println("Value of x (double): " + x);
        System.out.println("Value of y (int after casting): " + y);
    }
}
